#include<iostream>
using namespace std;

int main(){

    cout<<(5<10)<<endl; //true->1
    cout<<(5>10)<<endl; //false->0
    cout<<(5<=10)<<endl; //true->1
    cout<<(5>=10)<<endl; //false->0
    cout<<(5==10)<<endl; //false->0
    cout<<(5!=10)<<endl; //true->1(!->Not Equal)
    cout<<(5!=5)<<endl; //false->0

    return 0;
}
